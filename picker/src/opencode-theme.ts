export type OpenCodeThemeRole = keyof typeof opencodeThemeRoles

export function resolveOpenCodeTheme(mode: OpenCodeThemeMode): Record<OpenCodeThemeRole, string> {
  return Object.fromEntries(
    Object.entries(opencodeThemeRoles).map(([role, value]) => {
      const color = value[mode]
      return [role, color in opencodeThemeDefs ? opencodeThemeDefs[color as keyof typeof opencodeThemeDefs] : color]
    }),
  ) as Record<OpenCodeThemeRole, string>
}

